   <h1>After the 33rd Lingfield Marathon Fun Run ...</h1><br/>
<p>It is with mixed feelings of pride, joy, humility, showing a great community spirit and sadness, that the Lingfield marathon Fun Run will not take place in 2016.</p>
<p>The committee feel that the Fun Run may have run its course. (Pun hopefully intended)</p>
<p>It started in 1981 as a project for the International Year of Disabled People.</p>
<p>Tandridge District was divided into 2 parts;- Tandridge North to construct a pedestrian Tunnel at Oxted Railway station for better access, and Tandridge South to provide a minibus with disabled access so that everyone would have an inexpensive method to get about.- The Lingfield Marathon Minibus.</p>
<p>Both projects were completed within 5 years.</p>
<p>In 1987 came the Great Storm causing millions of Pounds damage in which the Lingfield marathon Minibus was crushed by 5 trees.  This was a great loss to our area and more so to the elderly & disabled people without the use of a car and also to many group activities.</p>
<p>So the Lingfield Marathon Fun Run continued to fund a replacement.</p>
<p>By 2014 our 5th Minibus was purchased for just under Â£40,000 thanks to all the generosity of volunteers, businesses, organisations and all the entrants in the walks & runs.</p>
<p>During those 33 years we have involved many volunteers, organisations, charities, businesses and the public in raising many thousands of pounds which in turn have provided 5 minibuses and helped over 200 local groups and associations with their projects.</p>
<p>The number of entrants has been falling in recent years; in our best year we had over 1200 entrants in 2015 we were down to 230. Other local events have ended: the Firework & Bonfire Parade and the Lingfield Evening. IS IT NOW THE END OF THE LINGFIELD MARATHON AS WELL?</p>
<p>We will need other fund raising to keep the Marathon Minibus going into the future.</p>
	<p>Graham Marks (Chairman)</p>

   
     
 	 

