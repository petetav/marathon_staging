<p>The Lingfield Marathon Minibus, which is maintained through local fund raising and grants, has been in continuous service to the community since 1983.</p>

<p>The current bus, a Peugeot Boxer conversion professionally 'sign written', with a new and distinctive livery, entered service in 2014. It is fitted with modern electronic extras including air conditioning and meets all VOSA certificated safety specifications. Wheelchair access is made easy with an electrically operated winch and a manual ramp.</p>

<p>With a 13 seat capacity (+driver), the bus can be driven by volunteers holding a full driving licence. All new drivers are required to have a familiarisation tour of the bus and must provide driving licence details prior to usage.</p>



<p>The bus runs, with the help of volunteer drivers who operate a weekly service to the Lingfield Lunch Club followed by the Derby and Joan Club both of which are held on a Tuesday at the Lingfield and Dormansland  Community Centre. Twice monthly shopping trips, one to Oxted and one to Crawley are also provided by volunteer drivers. Details of these services can be obtained from the Lingfield and Dormansland Community Centre, 
	<a href="mailto:enquiries@lingfieldcentre.org">enquiries@lingfieldcentre.org</a>
	
	 or on 01342-833893.</p>
 
<p>The bus is also available for charity work in the Lingfield area and for hire.</p>

